/*
4. Присвоить переменной а значение в промежутке [0..15]. С помощью оператора switch организовать вывод чисел от a до 15.
*/
let a = 15,
  b;

switch (a) {
  case 1:
    b = 'один';
    break;
  case 2:
    b = 'два';
    break;
  case 3:
    b = 'три';
    break;
  case 4:
    b = 'четыре';
    break;
  case 5:
    b = 'пять';
    break;
  case 6:
    b = 'шесть';
    break;
  case 7:
    b = 'семь';
    break;
  case 8:
    b = 'восемь';
    break;
  case 9:
    b = 'девять';
    break;
  case 10:
    b = 'десять';
    break;
  case 11:
    b = 'одиннадцать';
    break;
  case 12:
    b = 'двенадцать';
    break;
  case 13:
    b = 'тринадцать';
    break;
  case 14:
    b = 'четырнадцать';
    break;
  case 15:
    b = 'пятьнадцать';
    break;
}
console.log(b);
